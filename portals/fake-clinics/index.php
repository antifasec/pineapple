Fake abortion clinics: the threat to reproductive self-determination.

Mertus JA(1).

Author information:
(1)United States District Court, Southern District of New York.

The establishment of "fake abortion clinics" poses a great threat to women's 
ability to make free and informed procreative decisions. Such clinics 
intentionally deceive pregnant women into believing that they provide a full 
range of women's health services when, in reality, they provide only a pregnancy 
test, accompanied by intense anti-abortion propaganda. Because fake abortion 
clinics threaten women's interests in "privacy" and decisional autonomy, state 
attorneys general should challenge them under deceptive business practice 
statutes. Successful challenges can be brought without violating anti-abortion 
groups' First Amendment rights.

PIP: The establishment of "fake abortion clinics" poses a dire threat to women's 
choice-making ability. These clinics purposely deceive women into thinking that 
they are providing a range of women's health services. All they provide is a 
pregnancy test and much anti-abortion propaganda. Women who go to these centers 
may feel much psychological stress. There are over 1000 of these clinics in the 
US. Only a few have been challenged by private parties or attorneys general. 
States' attorneys general should take more action under unfair business practice 
laws. This can be done without violating the 1st amendment's free speech 
protection. Fake abortion clinics fall under the "intentionally false and 
deceitful" speech category. This is beyond the 1st amendment's scope. The 
deceptive activities take place inside and outside the center. Their advertising 
is misleading. They make pregnant women see an anti-abortion movie before they 
get the results of their pregnancy tests. 2 "privacy" issues are at stake here. 
The 1st is that the content and risks are never disclosed. The 2nd is that the 
abortion decision should be independently made. The few cases that have been 
challenged under state deceptive business laws have been successful. The 1st 
amendment protects commercial speech from unwarranted government regulation. The 
State Court has not said what a definition of commercial speech is. The Court 
takes into consideration the format and content of the speech. Fake abortion 
clinic advertising is commercial. Even if their ads were found to be 
noncommercial speech, they could still be banned without being contrary to the 
1st amendment. Fake abortion clinics' ads do not get any 1st amendment 
protection because they are "misleading and deceptive." Advertising may be 
brought under control of law if: 1) the government's interest in this is great; 
2) the laws directly advance the government's asserted interest; and 3) the 
restrictions are no more extensive than they have to be. The government might 
want to regulate speech in fake abortion clinics because it wants to prevent 
deceptive business rules. It might want to prevent women from getting into a 
situation where they are coerced. Enforcing deceptive business practice laws 
against anti-abortion clinics would directly advance the interests of the states 
and allow for free decision making on reproductive choices.

